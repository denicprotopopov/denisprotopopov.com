					<div>
						<h1>Data Is Finally Over (2020)</h1>
					</div>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<div>
						<h3>Privacy Does Not Make Sense</h3>
					</div>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<p>
						Very fast return to the status quo after the Cambridge Analytica data scandal has shown
						that the Internet is not going anywhere. The Internet is still going to be centralized
						around vectoralists - the ruling class that makes data its property and a product. This
						class was described in "A Hacker Manifesto" by McKenzie Wark by analogy with the
						bourgeoisie.
					</p>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<p>
						While promising something completely different, the Internet industry has created
						hegemony of control and centralization that is lead by Google, Apple, Facebook, and
						Amazon. There are ways to find a reason for this scenario. For example, Alexander
						Galloway in 'Protocols' argues that while TCP/IP protocol creates a horizontally
						distributed network, the DNS has vertically stratified logic. But this glance to the
						past describes the current state of affairs.
					</p>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<p>
						Two narratives prevail on the Internet: Chinese and American. In the last one, power is
						concentrated in the hands of a small group of corporations. The Chinese approach
						declares governmental control, where there is no place for non-domestic services. It
						seemed like Europe could offer an alternative. Attempts of the European Union to protect
						the competitiveness of software and protection of user personal data gave reason to
						believe that there might be 'something decentralized'. Well, we are witnessing that in
						the current situation, you can't help but choose only between the American or the
						Chinese. As a result, almost the only two developers of mobile operating systems, Apple
						and Google collaborate on the project that develops the protocol for contact tracing
						during the novel coronavirus outbreak. The tracing functionality will be built right
						into the operating system.
					</p>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<p>
						The current epidemiological view on society radically changed the attitude to this
						project. The fact that two major corporations collaborate to create a contact tracing
						system built-in on the OS level was very welcomed. Outrage about 'total surveillance'
						would be a natural response before the spring of 2020. But now this response is almost
						as inappropriate as a violation of social distancing. Essentially, the Internet activist
						rhetorics of the 90s have lost their voice and even sense.
					</p>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<p>
						Nobody 'quit' Facebook after Cambridge Analytica, nobody stopped using Zoom because of
						their privacy issue. Maybe we had come to understand how quantitative factors of data
						distract from other, unaccountable properties of the new reality. We have witnessed that
						'virtual' automatization turned out more durable than "real". How data and privacy exist
						in this paradigm? Maybe there is no such thing as data?
					</p>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<div>
						<h3>Is It Data?</h3>
					</div>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<p>
						In her books and articles (e.g. 'Graphesis: Visual Forms of Knowledge Production', <i
							>Cambridge: Harvard University Press, 2014</i
						>, 'Humanities Approaches to Graphical Displays',
						<i>Digital Humanities Quarterly 5.1, 2011</i>) Johanna Drucker exposes what we consider
						data as a construct. Data, which is derived from the Latin word 'dare', meaning 'to
						give', should be considered as the total sum of facts that it is possible to know about
						a person or an entity. The conventional use of data implies prejudices and leaves no
						room for its critical interpretation and concerns.
					</p>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<p>
						This quantitive view on information and its capturing puts only 'How?'-question, that
						cannot be switched to more important questions that help to critically observe Internet
						and algorithmisazation: 'Why?', 'Who?', 'What?'. This point of view states privacy as a
						norm of preservation data in the previously mentioned context: as everything that it is
						possible to know about that person. But data that is being collected by Facebook or
						Google has completely different properties that are not covered by
						'quantitative-self-evident, value neutral, and observer-independent' point of view.
					</p>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<p>
						Drucker states that data is actually <i>capta</i>. This word derives from Latin verb
						capio, which means 'to capture, seize, take'. Latin capere, in a figurative sense, could
						mean 'to understand'. This term is not about bringing qualitative into quantitative; it
						is what is selectively measured. This redefinition precisely establishes that the act of
						capturing facilitates our understanding of information. Thus capta are units of
						information that have been taken from the sum of <i>all potential data</i>. Using this
						term, it is possible to rename, for example, databases to captabases - as a collection
						of capta that can be accessed, written, rewritten, etc.
					</p>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<p>
						Capta is an expression of information understood as constructed, thus, more complex.
						This particular redefinition declares a shift to uncountable factors of social
						formatting by digital. As an element of infrastructure and information flows, privacy is
						also 'included' in capta. But what is a privacy right now? Can violation of privacy and
						measurement of capta be considered as surveillance?
					</p>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<div>
						<h3>City Consists Of Code</h3>
					</div>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<p>
						Spring of 2020 has shown us that the Internet was not just a layer of automatization
						above city infrastructure. People can still live in cities, because infrastructure
						constantly works on multiple levels: they have stable Internet connection and
						infrastructure, food tech companies deliver meals that were cooked on cloud kitchens.
						Something that was considered as a virtual 'add-on' to reality started to exist as a
						natural part of reality. Here we should begin to ignore the concept of data as an
						observer-independent phenomenon. Capta reveals <i>processes</i> and their meanings. We realized,
						that we cannot disconnect from these processes and we see how technologies become abstract,
						intimate and invisible. It is possible to see what exists beyond capta collection and privacy
						and take into consideration technological infrastructures, hard drives, cables, and sysadmins.
					</p>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<p>
						We cannot critique Apple and Google for 'surveillance', because privacy, capta, and
						users are distributed. Cities found themselves interlinked with flows of information and
						vice versa. There is no longer a simple paradigm where a client requests to the server.
						We observe the reality where automatization is not a set of simple manageable
						frameworks; capta is blended, privacy is distributed. It is distributed among
						smartphones, CCTV cameras, thermal imaging, couriers, pick-up-point workers at
						Wildberries that want to protect the privacy of their <i>data</i> and get protective equipment
						and disinfection.
					</p>
				</div>
			</div>
