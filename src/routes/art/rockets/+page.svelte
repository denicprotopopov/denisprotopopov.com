				<div class="cell">
					<h3>Algorithm, randomizer — why such difficulties at all?</h3>
					<p><b>Vanya Demidkin:</b></p>
					<p>
						In this performance, we work with a sense of the time we are in — a sense of the
						connection of everything with everything, that all random things are not really random,
						that everything is mounted with everything in any scenario. How can this principle be
						implemented in reality? This is where the idea of the algorithm and the design of the
						performance itself arose as a random sequence of elements that changes every time. Why?
						Because in the same way our whole reality is changing around, being assembled from a
						huge number of circumstances, accidents, meetings, relationships, communications. The
						algorithm is the embodiment of this principle.
					</p>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<p>
						But there are additional circumstances. For example, our own interest in watching our
						own performance and not getting tired of it. If we were doing a classic performance in
						which there is a beginning, middle and end, then by the fifth show we would already be
						uninterested to watch, or the eye would be blurred and we could not adequately evaluate
						our work. So, by the very principle of the performance, we have created such a structure
						that gives us the opportunity to enjoy this project, watch it as an audience. We love a
						certain type of theater and want to see it in Russia — and so we staged such a
						performance and created an opportunity for ourselves to watch it from the outside.
					</p>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<p>
						And by the way, this is also an opportunity to bring the performers of the performance
						out of the state of automatism. They can't just go on stage every month, play and leave,
						no — it requires a lot of preparation, a lot of responsibility and a conscious approach
						to each specific show.
					</p>
				</div>
			</div>

