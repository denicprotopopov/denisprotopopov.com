						And on the morning of the deadline, I talked to Olya Tarakanova, told her how everything
						was going on, that we still didn't have a name, and Olya quoted Elena Kovalskaya, who
						was just telling her: rockets, they don't fly, they take off and scatter in the air.
						This is about dreams, about how things actually happen, about the fact that the utopian
						potential of any subject is very different from the final outcome. I thought, damn,
						that's a great name for a play. We had a laugh, and in the evening I met with Denis and
						Elina and said — maybe we should call it that? And they're like, come on, nice name! And
						it so happened that it was from the title that the plays and many other details of the
						play eventually came out. So the title of our performance rather simply poetizes the
						principle that things are both what they are and what they could be. This phrase can
						describe anything on the planet, and it's very cool.
