					<h3>
						The order of the scenes in the performance is determined by the algorithm. How does it
						work?
					</h3>
					<p><b>Denis Protopopov</b></p>
					<p>
						The algorithm for our performance is written in JavaScript — we used it to easily access
						the play just in a browser window. How it works: three arrays are created in it, and
						each array contains elements — strings, pieces from the play. In the first play there
						are 34 such pieces, in the second and in the third — 4 pieces each. Then I wrote some
						functions. The first one selects 70% of the elements from each array and puts it all in
						a common large array. And the second function simply shuffles the contents of the shared
						array and displays a new text on the screen. We used the Math Random method as a
						randomizer. I also inspired Vanya to learn programming languages, and Vanya made a
						second algorithm in Python with an array of names of actors and performers. Before each
						show, we run not only the main algorithm that generates the play, but also the second
						algorithm — it selects the person who will represent the objects on the bows at the end.
					</p>
				</div>
			</div>

