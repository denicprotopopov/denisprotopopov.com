						By the way, about the artists and the objects on the bows above. Why do you attach such
						importance to things?
					</h3>
					<p><b>Artem Tomilov:</b></p>
					<p>
						Simply put, things are equal to people. For us, this is an exercise to feel nonvertical:
						after all, rockets are about strength, vertical, and treating things as equals is about
						responsibility and awareness, about another world in general. On the one hand — the
						monster of capitalism with its overgrowth of things, on the other — things as objects
						that are here and now with us on an equal footing, and not just a background around. 
						After all, for the one who launches missiles, the enemy is an abstract background that
						needs to be destroyed, for him the surrounding phenomena are weakened as subjects,
						that's why the whole experience of the cast gap between everyone.
					</p>
