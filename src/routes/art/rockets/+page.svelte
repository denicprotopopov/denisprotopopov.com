					<h3>I see a composer in the list of creators. How is the music arranged in the play?</h3>
					<p><b>Elina Lebedze:</b></p>
					<p>
						The music in "Rockets" is also arranged as a constructor: there is a set of sound lines,
						each of which is attached to a certain repeating detail of the text. Such a solution
						appeared immediately, even at the sketch stage. I wanted to entrust these lines to live
						instruments and had already managed to record some of them, but the self-isolation mode
						violated the plans, I remained locked at home. Then I decided to simply delegate the
						filling of some lines to the sounds that I would find in my apartment. I walked around
						the house with a microphone, looking for sounds in everyday objects that would resonate
						with the plays. It was a very pleasant job, I learned a lot about my things: for
						example, it turns out that I have a cool candlestick from which you can extract a
						billion very beautiful rustles and tones.
					</p>
				</div>
			</div>

			<div class="row">
				<div class="cell">
					<p>
						Another important element of the musical fabric is two live voices. Together with the
						vocalists of the La Gaul theater, Lera and Katya, we found and came up with different
						ways of sound production, immediately tried them, and such a mini-laboratory turned out.
						Before each performance, we reassemble the score from the pieces according to the
						algorithm, and Katya and Lera sing according to this score.
					</p>
