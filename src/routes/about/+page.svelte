                    <h3>Meyerhold Center</h3>
                    <p><b>2020 — 2022</b></p>
                    <p><b>Resident</b></p>
                    <p>After winning the Blackbox residency, I became a permanent resident of the Meyerhold Center, one of the most important theatrical and artistic platforms in Russia. Here, I conceptualized and directed the algorithmic theater performance "Rockets Take Off and Crash and Scatter In the Air," which successfully ran for two theatrical seasons.</p>
                    <p>During my time at the Meyerhold Center, I produced and developed the "Digital Theater Workshop," the first online course on digital arts in Russia.</p>
                    <p>I was no longer a resident of the Meyerhold Center when the theater was closed by the Russian government due to the anti-war position of its director.</p>
