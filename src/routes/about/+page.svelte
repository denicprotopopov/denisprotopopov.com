                <div class="cell">
                    <h3>Yandex Practicum</h3>
                    <p><b>2021 — now</b></p>
                    <p><b>Learning Experience Designer</b></p>
                    <p>I joined a team where I was responsible for the learning experience of five web development products. During my time here, I developed and implemented a pipeline that utilizes student behavior data to execute metric-based content refactoring, which helped to maintain a completion rate of over 50% and an employability rate of over 70%.</p>
                    <p>I also redesigned the curriculum and learning experience of two online reskilling bootcamps: "Frontend Developer" and "Fullstack Developer". Currently, I consult with the Practicum team on the learning experience of the "Soft Skills for Developers" course.</p>
