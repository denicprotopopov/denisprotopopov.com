                    <h3>Freelance Artist and Developer</h3>
                    <p><b>2017 — now</b></p>
                    <p>Back in college, I utilized my development skills to create artistic projects and theater performances. I have developed websites, mobile apps, and games using JavaScript, Svelte, Three.js, Swift, and Unity.</p>
